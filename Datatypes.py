#Getting the Data Type with type() function
x = 5
print(type(x))

#Data types
#Data Type = str
x = "Hello World"   
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = int
x = 20  
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = float
x = 20.5   
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = complex
x = 3j  
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = list
x = ["apple", "banana", "cherry"]  
print(x)            #display x:
print(type(x))      #display the data type of x:


#Data Type = tuple
x = ("apple", "banana", "cherry")  
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = range
x = range(6) 
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = dict
x = {"name" : "John", "age" : 36} 
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = set
x = {"apple", "banana", "cherry"}
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = frozenset
x = frozenset({"apple", "banana", "cherry"}) 
print(x)            #display x:
print(type(x))      #display the data type of x:

#Data Type = bool
x =True 
print(x)            #display x:
print(type(x))      #display the data type of x:
